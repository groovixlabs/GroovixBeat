 LoadDrumPatternNames()
{
    let str="<option value=''>Select</option>";
    for(let i=0;i<this.g_DrumPatterns.length;i++)
    {
        str+=`<option value="${i}">${this.g_DrumPatterns[i].name}</option>`;
    }
    //$("#DrumPatternName").html(str);
    return str;
}



 GetDrumPattern(drumpattern)
{
    let DrumPatternStyle=this.g_DrumPatterns[drumpattern]?.styles ?? [];

    let str="<option value=''>Select</option>";
    for(let i=0;i<DrumPatternStyle.length;i++)
    {
        str+=`<option value="${i}">${DrumPatternStyle[i].name}</option>`;
    }

    return str;
}

 GetDrumPatternStyle(drumpattern,drumstyle)
{
    //let drumpattern=$("#DrumPatternName").val();
    //let drumstyle=$("#DrumPatternStyle").val();
    let pat=this.g_DrumPatterns[drumpattern].styles[drumstyle].data;
    
    //console.log(pat);
    
    let ptable=this.PatToTable(pat)
    //$("#DrumPatternTable").html(ptable);
    return ptable
}

PatToTable(pat)
{
    let table="<table class='drumpat_table'>\n";
    pat=pat.trim();

    //$("#DrumWizardText").val(pat);

    let patA=pat.split("\n");
    for(let i=0;i<patA.length;i++)
    {
        //let line = patA[i].replace(/\s+/g, '');
        //console.log(line);

        let noteA=patA[i].split("|");
        if (noteA[1][0]=='-') continue;
        table+="<tr>";

        let prclass='';
        let note=noteA[1].trim();
        if (i==0) prclass+=' drumpat_headertd ';
        if (note=='AC') prclass+=' drumpat_accent ';

        for(let j=1;j<noteA.length-1;j++)
        {
            let note=noteA[j].trim();

            let title=chords.drumAbbreviationMappings[note]?.name ?? note;
            if (j==1) note=title;


            let pclass=prclass;
            if (note=='X') pclass=' drumpat_filled '
            if (j==1) pclass=' drumpat_titlecell '
            

            table+=`<td class='drumpat_td ${pclass}'>`+note;
            //console.log(noteA[j]);
         
            table+="</td>";
        }
        table+="</tr>\n";
    }

    table+="</table>";
    return table;
}



